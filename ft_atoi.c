int	ft_atoi(const char *str)
{
	int	i;
	int	nb;
	int	isneg;

	i = 0;
	nb = 0;
	isneg = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
		{
			isneg *= -1;
		}
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + str[i] - 48;
		i++;
	}
	return (nb * isneg);
}
