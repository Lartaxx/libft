#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	while (n > 0)
	{
		((char *)str)[i] = c;
		i++;
		n--;
	}
	return (str);
}
